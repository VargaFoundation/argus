    "Employee Name"       VARCHAR(50),
    "Employee Dept"       VARCHAR(4),
    "Manager Name"        VARCHAR(255),
    "Employee Yrs Exp"    DOUBLE,
    "Employee Salary"     DECIMAL(18,4),
    "Customer Name"       VARCHAR(50),
    "Customer State"      VARCHAR(50),
    "Call Center Region"  VARCHAR(25),
    "Customer Balance"    DOUBLE,
    "Customer Segment"    VARCHAR(25),
    "Prod Type1"          VARCHAR(50),
    "Prod Type2"          VARCHAR(50),
    "Prod Type3"          VARCHAR(50),
    "Prod Type4"          VARCHAR(50),
    "Product Name"        VARCHAR(100),
    "Product Container"   VARCHAR(25),
    "Ship Promo"          VARCHAR(25),
    "Supplier Name"       VARCHAR(25),
    "Supplier Balance"    DOUBLE,
