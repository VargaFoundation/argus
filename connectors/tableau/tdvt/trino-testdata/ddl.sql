-- TDVT test tables (TestV1: Calcs + Staples), typed for Trino.
--
-- Target: the built-in `memory` connector — writable, no object store, perfect
-- for a TDVT run. Enable it once by adding, in the Trino coordinator,
--   etc/catalog/memory.properties  →  connector.name=memory
-- (the trinodb/trino image: mount or exec-write that file, then restart), then:
--   trino> CREATE SCHEMA IF NOT EXISTS memory.tdvt;
-- and run this DDL. load.py then fills the tables from the SDK CSVs.
--
-- Type mapping from the generic TestV1 DDL: DOUBLE PRECISION→DOUBLE,
-- NUMERIC(18,4)→DECIMAL(18,4); the rest are identical to Trino's names.

CREATE TABLE IF NOT EXISTS memory.tdvt."Calcs" (
    "key"       VARCHAR(255),
    "num0"      DOUBLE,
    "num1"      DOUBLE,
    "num2"      DOUBLE,
    "num3"      DOUBLE,
    "num4"      DOUBLE,
    "str0"      VARCHAR(255),
    "str1"      VARCHAR(255),
    "str2"      VARCHAR(255),
    "str3"      VARCHAR(255),
    "int0"      INTEGER,
    "int1"      INTEGER,
    "int2"      INTEGER,
    "int3"      INTEGER,
    "bool0"     BOOLEAN,
    "bool1"     BOOLEAN,
    "bool2"     BOOLEAN,
    "bool3"     BOOLEAN,
    "date0"     DATE,
    "date1"     DATE,
    "date2"     DATE,
    "date3"     DATE,
    "time0"     TIMESTAMP,
    "time1"     TIME,
    "datetime0" TIMESTAMP,
    "datetime1" VARCHAR(255),
    "zzz"       VARCHAR(255)
);

